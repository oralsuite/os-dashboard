'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { cn } from '@/lib/utils';
import { useAuth } from '@/lib/auth';

const dentistNavItems = [
  { href: '/dashboard', label: 'Inicio', icon: HomeIcon },
  { href: '/dashboard/orders', label: 'Órdenes', icon: OrdersIcon },
  { href: '/dashboard/patients', label: 'Pacientes', icon: PatientsIcon },
  { href: '/dashboard/laboratories', label: 'Laboratorios', icon: LabIcon },
  { href: '/dashboard/chat', label: 'Mensajes', icon: ChatIcon },
  { href: '/dashboard/profile', label: 'Perfil', icon: ProfileIcon },
];

const laboratoryNavItems = [
  { href: '/dashboard', label: 'Inicio', icon: HomeIcon },
  { href: '/dashboard/orders', label: 'Órdenes', icon: OrdersIcon },
  { href: '/dashboard/chat', label: 'Mensajes', icon: ChatIcon },
  { href: '/dashboard/profile', label: 'Perfil', icon: ProfileIcon },
];

export function Sidebar() {
  const pathname = usePathname();
  const { user } = useAuth();

  const navItems = user?.role === 'LABORATORY' ? laboratoryNavItems : dentistNavItems;

  return (
    <aside className="fixed left-0 top-0 z-40 h-screen w-64 border-r border-gray-200 bg-white">
      <div className="flex h-full flex-col">
        {/* Logo */}
        <div className="flex h-16 items-center gap-2 border-b border-gray-200 px-6">
          <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-blue-600">
            <svg className="h-5 w-5 text-white" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19.428 15.428a2 2 0 00-1.022-.547l-2.387-.477a6 6 0 00-3.86.517l-.318.158a6 6 0 01-3.86.517L6.05 15.21a2 2 0 00-1.806.547M8 4h8l-1 1v5.172a2 2 0 00.586 1.414l5 5c1.26 1.26.367 3.414-1.415 3.414H4.828c-1.782 0-2.674-2.154-1.414-3.414l5-5A2 2 0 009 10.172V5L8 4z" />
            </svg>
          </div>
          <span className="text-lg font-bold text-gray-900">OralSuite</span>
        </div>

        {/* Navigation */}
        <nav className="flex-1 overflow-y-auto p-4">
          <ul className="space-y-1">
            {navItems.map((item) => {
              const isActive = pathname === item.href ||
                (item.href !== '/dashboard' && pathname.startsWith(item.href));

              return (
                <li key={item.href}>
                  <Link
                    href={item.href}
                    className={cn(
                      'flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors',
                      isActive
                        ? 'bg-blue-50 text-blue-700'
                        : 'text-gray-600 hover:bg-gray-50 hover:text-gray-900'
                    )}
                  >
                    <item.icon className="h-5 w-5" />
                    {item.label}
                  </Link>
                </li>
              );
            })}
          </ul>
        </nav>

        {/* User role indicator */}
        <div className="border-t border-gray-200 p-4">
          <div className="rounded-lg bg-gray-50 px-3 py-2">
            <p className="text-xs text-gray-500">Rol</p>
            <p className="text-sm font-medium text-gray-900">
              {user?.role === 'DENTIST' ? 'Dentista' : user?.role === 'LABORATORY' ? 'Laboratorio' : 'Admin'}
            </p>
          </div>
        </div>
      </div>
    </aside>
  );
}

